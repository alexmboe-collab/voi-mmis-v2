<?php

/**
 * ==========================================================
 * Dashboard Statistics
 * ==========================================================
 */

function dashboardStats(PDO $pdo)
{
    $stats = [];

    /*
    |--------------------------------------------------------------------------
    | USERS
    |--------------------------------------------------------------------------
    */

    $stmt = $pdo->query("SELECT COUNT(*) FROM users");

    $stats['users'] = (int) $stmt->fetchColumn();

    return $stats;
}